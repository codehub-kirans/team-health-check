<h1>Welcome to the Team Health Check App!</h1>
<article>
	<p>
		The health check app has a set of behaviour anchors on relevant themes for a high performing
		team to discuss and capture sentiment. For each question or theme, the team/squad discusses if
		they are closer to “awesome” or closer to “bad”, which colour to choose for that indicator, and
		what the trend is (stable, improving, or getting worse).For example, the theme "can we release
		easily?" might elicit responses of "our releases are good/meh/bad" i.e. green, amber and red,
		the colours of a traffic light.
	</p>
	<p>
		<b style="color: green;">GREEN</b>: Things are going very well. Although this does not mean that
		everything is perfect the team or squad is satisfied and does not see much space for
		improvement. <br />
		<b style="color: #ffcc00 ">AMBER</b>: There are some problems that need to be solved. However,
		it is not a disaster.
		<br />
		<b style="color: red;">RED</b>: There is a lot going wrong. Improvements are urgently needed.
		<br />
	</p>
	<p>
		The app collects input from everyone in real time and results are instantly tallied. The app can
		also keep a track of the squads progress over time without the need for manual collation. Sort
		responses by most positive, most negative or most mixed and facilitate better discussions.
	</p>
</article>
<article>
