<h1>Welcome to the Team Health Check App!</h1>
<p>
  The health check app has a set of themes for the team to discuss and capture
  their sentiment about each theme. For example, the theme "can we release
  easily?" might elicit responses of "our releases are awesome/meh/crappy" i.e.
  green, amber and red, the colours of a traffic light. The app collects input
  from everyone in real time and results are instantly tallied. The app can also
  keep a track of the squads progress over time without the need for manual
  collation. Sort responses by most positive, most negative or most mixed and
  facilitate better discussions.
</p>
