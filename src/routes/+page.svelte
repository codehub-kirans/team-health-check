<h1>Welcome to the Team Health Check App!</h1>
<p>
	The health check app has a set of questions on themes for the team to discuss and capture their
	sentiment about each theme. For each question, the squad is asked to discuss if they are closer to
	“awesome” or closer to “crappy”, and we use basic workshop techniques (dot voting, etc) to help
	them reach consensus about which color to choose for that indicator, and what the trend is
	(stable, improving, or getting worse).For example, the theme "can we release easily?" might elicit
	responses of "our releases are awesome/meh/crappy" i.e. green, amber and red, the colours of a
	traffic light.
</p>
<p>
	The app collects input from everyone in real time and results are instantly tallied. The app can
	also keep a track of the squads progress over time without the need for manual collation. Sort
	responses by most positive, most negative or most mixed and facilitate better discussions.
</p>
